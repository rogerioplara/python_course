""""
str - string
textos que estão entre aspas são strings
"""

print('Aspas simples')
print("Aspas duplas")
print(123456)  # tipo de dado primitivo interpretado como dado;
print('123456')  # interpretado como string;

print("Essa é uma 'string' (str).")
print('Essa é uma "string" (str).')
print("Esse é meu \"texto\" (str).")  # a barra invertida ignora o carácter;
print('Esse é meu \'texto\' (str).')  # a barra invertida ignora o carácter;
print('Esse é meu \n (str).')  # \n serve para pular linha;
print(r'Esse é meu \n (str).')  # o r antes da string não deixa executar o comando \ na string;
