# função print serve para mostrar informações na tela;
print(123456)  # 123456 é um argument e tem um tipo;

print('Rogerio', 'Peres', 'Lara')  # Por padrão, é adicionado espaço entre concatenações de argumentos;

print('Rogerio', 'Peres', sep='-')  # sep= é o separador, serve para substituir o espaço pelo que vier após o sep=;
print('Rogerio', 'e', 'Priscilla', sep='-', end='###')  # end= substitui o que vem no fim do comando print;
print('Rogerio')

print('072', end='.')
print('357', '779', sep='.', end='-')
print('07')
